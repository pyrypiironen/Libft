#include "libft.h"

char	*ft_strcat(char *str1, const char *str2)
{
	int i;
	int y;

	i = 0;
	y = 0;
	while (str1[i])
		i++;
	while (str2[y])
	{
		str1[i] = str2[y];
		i++;
		y++;
	}
	i++;
	str1[i] = '\0';

	return (str1);
}
